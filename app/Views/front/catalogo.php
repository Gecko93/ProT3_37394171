        <h5 class="card-title">Empanadas de Pollo</h5>
        <p class="card-text">Ingredientes:
		1 paquete de masa de empanada sin gluten
		2 pechugas de pollo cocidas y desmenuzadas
		1 cebolla picada
		1 pimiento rojo picado
		2 dientes de ajo picados
		100 g de aceitunas verdes picadas
		2 huevos duros picados
		Aceite de oliva
		Sal y pimienta al gusto</p>

		<p>Preparación:
		Precalienta el horno a 200°C.
		En una sartén, sofríe la cebolla, el pimiento y el ajo hasta que estén tiernos.
		Añade el pollo desmenuzado, las aceitunas y los huevos duros. Salpimienta al gusto y mezcla bien.
		Rellena las empanadas con la mezcla y cierra los bordes.
		Pinta las empanadas con un poco de huevo batido y hornea durante 15-20 minutos o hasta que estén doradas</p>
