      <div class="card-body">
        <h5 class="card-title">Panqueques</h5>
        <p class="card-text">Ingredientes:
		200 g de premezcla sin TACC
		2 huevos
		1 cucharadita de Aceite
		250 cc de leche (o agua)</p>
		<p>Preparación:
		En un bol grande, mezcla la harina sin gluten, el azúcar y la sal.
		En otro recipiente, bate el huevo y añade la leche, el aceite (o mantequilla derretida) y la esencia de vainilla.
		Vierte los ingredientes líquidos en los ingredientes secos y mezcla bien hasta obtener una masa homogénea y sin grumos. Si la masa está muy espesa, añade un poco más de leche hasta alcanzar la consistencia deseada.
		Calienta una sartén antiadherente a fuego medio y engrásala ligeramente con aceite o mantequilla.
		Vierte un poco de masa en la sartén caliente y extiende con movimientos circulares para formar un panqueque delgado.
		Cocina hasta que aparezcan burbujas en la superficie y los bordes empiecen a despegarse, luego dale la vuelta y cocina el otro lado hasta que esté dorado.
		Repite el proceso con el resto de la masa.
		Sugerencias de Servido:

		Puedes acompañar los panqueques con frutas frescas, miel, jarabe de arce, mermelada o crema batida.
		Para una versión salada, omite el azúcar y la vainilla, y añade un poco más de sal. Rellénalos con queso, jamón, espinacas, etc.
		</p>
