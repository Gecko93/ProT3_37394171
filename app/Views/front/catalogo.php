      <div class="card-body">
        <h5 class="card-title">Pan sin gluten</h5>
        <p class="card-text">500 g de harina sin gluten (puedes usar una mezcla de harina de arroz, maicena y almidón de tapioca)
		10 g de sal
		10 g de azúcar
		10 g de levadura seca
		450 ml de agua tibia
		2 cucharadas de aceite de oliva
		1 cucharadita de vinagre de manzana</p>
		<p>Preparación: En un bol grande, mezcla la harina sin gluten, la sal y el azúcar.
		Disuelve la levadura en el agua tibia y déjala reposar durante 5-10 minutos hasta que empiece a formar espuma.
		Añade el aceite de oliva y el vinagre de manzana a la mezcla de agua y levadura.
		Vierte los ingredientes líquidos en los secos y mezcla bien hasta obtener una masa homogénea.
		Coloca la masa en un molde para pan previamente engrasado y cubre con un paño limpio. Deja reposar en un lugar cálido durante 1 hora o hasta que doble su tamaño.
		Precalienta el horno a 200°C.
		Hornea el pan durante 35-40 minutos o hasta que esté dorado y suene hueco al golpearlo ligeramente.
		Deja enfriar sobre una rejilla antes de cortarlo</p>
