        <h5 class="card-title">Brownies</h5>
        <p class="card-text">Ingredientes:

			200 g de chocolate negro sin gluten
			150 g de mantequilla
			150 g de azúcar
			3 huevos
			75 g de harina de almendra
			1 cucharadita de esencia de vainilla</p>
		<p>Preparación:
			Precalienta el horno a 180°C.
			Derrite el chocolate y la mantequilla en un bol al baño maría.
			Añade el azúcar, los huevos y la esencia de vainilla, mezclando bien.
			Incorpora la harina de almendra y mezcla hasta obtener una masa homogénea.
			Vierte la masa en un molde y hornea durante 20-25 minutos.</p>
